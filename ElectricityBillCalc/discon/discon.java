package discon;

public class discon {

  public double meterToKm(double meters) {
  return meters / 1000;
  }

  public double kmToMeter(double km) {
  return km * 1000;
  }

  public double milesToKm(double miles) {
  return miles * 1.60934;
  }

  public double kmToMiles(double km) {
  return km / 1.60934;
  }
}
